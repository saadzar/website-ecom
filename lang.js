const LANGUAGE_STORAGE_KEY = "arkan-lang";
const SUPPORTED_LANGUAGES = new Set(["en", "fr", "ar"]);
let currentLang = "en";

function translate(key, lang = currentLang) {
  const entry = TRANSLATIONS[key];
  if (!entry) {
    return "";
  }

  return entry[lang] || entry.en || "";
}

function applyElementTranslations(lang) {
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.getAttribute("data-i18n");
    const value = translate(key, lang);
    if (!value) {
      return;
    }

    if (element.hasAttribute("data-i18n-html")) {
      element.innerHTML = value;
      return;
    }

    element.textContent = value;
  });

  document.querySelectorAll("[data-i18n-alt]").forEach((element) => {
    const value = translate(element.getAttribute("data-i18n-alt"), lang);
    if (value) {
      element.setAttribute("alt", value);
    }
  });

  document.querySelectorAll("[data-i18n-aria-label]").forEach((element) => {
    const value = translate(element.getAttribute("data-i18n-aria-label"), lang);
    if (value) {
      element.setAttribute("aria-label", value);
    }
  });

  document.querySelectorAll("[data-i18n-title]").forEach((element) => {
    const value = translate(element.getAttribute("data-i18n-title"), lang);
    if (value) {
      element.setAttribute("title", value);
    }
  });
}

function updateLanguageButtons(lang) {
  document.querySelectorAll(".lb").forEach((button) => {
    const buttonLang = button.dataset.lang || button.textContent.trim().toLowerCase();
    button.classList.toggle("on", buttonLang === lang);
  });
}

function setLang(lang) {
  const nextLang = SUPPORTED_LANGUAGES.has(lang) ? lang : "en";
  const isArabic = nextLang === "ar";

  currentLang = nextLang;

  try {
    localStorage.setItem(LANGUAGE_STORAGE_KEY, nextLang);
  } catch (_) {
    // Ignore storage errors.
  }

  document.documentElement.lang = nextLang;
  document.documentElement.dir = isArabic ? "rtl" : "ltr";

  const site = document.querySelector(".site");
  if (site) {
    site.classList.toggle("rtl-mode", isArabic);
  }

  updateLanguageButtons(nextLang);
  applyElementTranslations(nextLang);

  window.dispatchEvent(new CustomEvent("arkan:langchange", {
    detail: { lang: nextLang }
  }));
}

function getCurrentLang() {
  return currentLang;
}

function initLanguageButtons() {
  document.querySelectorAll(".lb[data-lang]").forEach((button) => {
    button.addEventListener("click", () => {
      setLang(button.dataset.lang);
    });
  });
}

function initLanguage() {
  initLanguageButtons();

  let savedLang = "en";
  try {
    const value = localStorage.getItem(LANGUAGE_STORAGE_KEY);
    if (value && SUPPORTED_LANGUAGES.has(value)) {
      savedLang = value;
    }
  } catch (_) {
    // Ignore storage errors.
  }

  setLang(savedLang);
}

window.setLang = setLang;
window.arkanI18n = {
  translate,
  setLang,
  getCurrentLang
};

if (document.readyState === "loading") {
  document.addEventListener("DOMContentLoaded", initLanguage, { once: true });
} else {
  initLanguage();
}
